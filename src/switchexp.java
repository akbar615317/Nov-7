public class switchexp {
    public static void main(String args[]) {
        String day = "";
        switch (day) {
            case "Monday":
                System.out.println("i need to go to gym");
                break;
            case "Tuesday":
                System.out.println("i need to go to office");
                break;
            case "Wednesday":
                System.out.println("i need to go to walk");
                break;
            default:
                System.out.println("do what ever u want");
                break;
        }
    }
}

